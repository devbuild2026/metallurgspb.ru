export function getProductBySlug(slug: string): Product | undefined {
  return products.find((product) => product.slug === slug);
}

export function getProductsByCategory(categorySlug: string): Product[] {
  return products.filter((product) => product.categorySlug === categorySlug);
}

export function getPopularProducts(limit = 8): Product[] {
  return products.slice(0, limit);
}

export function getDiscountProducts(): (Product & { discount: true; oldPrice: number })[] {
  const seen = new Set<string>();
  const result: (Product & { discount: true; oldPrice: number })[] = [];
  const factors = [1.18, 1.15, 1.20, 1.16, 1.17, 1.19, 1.15, 1.18];
  for (const p of products) {
    if (!seen.has(p.categorySlug)) {
      seen.add(p.categorySlug);
      const factor = factors[result.length] ?? 1.17;
      result.push({
        ...p,
        discount: true,
        oldPrice: Math.round(p.price * factor * 100) / 100,
      });
    }
    if (result.length >= 8) break;
  }
  return result;
}
