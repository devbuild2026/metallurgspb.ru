import React from 'react';

/**
 * Глобальная типографика для hero-заголовков (H1) и других заголовков.
 *
 * Решает три проблемы переноса строк в русских заголовках:
 *  1. Дефисные топонимы («Санкт-Петербург», «Красное-Село») — обычный дефис
 *     U+002D является точкой переноса, поэтому браузер рвёт «Санкт-» / «Петербурге».
 *     Такие токены оборачиваются в <span> с white-space: nowrap.
 *  2. Висячие предлоги/союзы/частицы («в», «на», «и», «по» …) — приклеиваются к
 *     следующему слову неразрывным пробелом (U+00A0), чтобы не оставались в конце строки.
 *  3. Тире («—», «–») — приклеивается к предыдущему слову неразрывным пробелом.
 *
 * Существующие неразрывные пробелы (U+00A0) в исходных строках сохраняются —
 * разбиение идёт только по обычным пробелам.
 *
 * SEO: видимый текст не меняется по составу символов. Обычный дефис (U+002D)
 * остаётся обычным дефисом, слова не подменяются — меняется только поведение
 * переноса. Поэтому H1-структура и ключевые фразы для поисковиков не ломаются.
 */

const NBSP = ' ';

// Короткие слова, которые нельзя оставлять в конце строки.
// Приклеиваются к СЛЕДУЮЩЕМУ слову неразрывным пробелом.
const SHORT_WORDS = new Set([
  'в', 'во', 'на', 'и', 'с', 'со', 'к', 'ко', 'по', 'из', 'изо',
  'от', 'ото', 'до', 'за', 'о', 'об', 'обо', 'у', 'а', 'но', 'не',
  'же', 'бы', 'ли', 'для', 'про', 'над', 'под', 'при', 'без', 'или',
]);

// Тире как отдельный токен (em-dash / en-dash). Дефис-минус сюда не входит.
const DASHES = new Set(['—', '–']);

// Токен содержит дефис между буквами/цифрами → составное слово/топоним, рвать нельзя.
const HYPHEN_COMPOUND = /[A-Za-zА-Яа-яЁё0-9]-[A-Za-zА-Яа-яЁё0-9]/;

export function polishHeroTitle(text: string): React.ReactNode {
  if (!text) return text;

  // Разбиваем только по обычным пробелам —   внутри токенов сохраняется.
  const tokens = text.split(' ').filter((t) => t.length > 0);
  const nodes: React.ReactNode[] = [];

  for (let i = 0; i < tokens.length; i++) {
    const token = tokens[i];

    // Разделитель ПЕРЕД текущим токеном.
    if (i > 0) {
      const prevIsShort = SHORT_WORDS.has(tokens[i - 1].toLowerCase());
      const curIsDash = DASHES.has(token);
      // короткое слово перед текущим ИЛИ текущий токен — тире → неразрывный пробел.
      nodes.push(prevIsShort || curIsDash ? NBSP : ' ');
    }

    if (HYPHEN_COMPOUND.test(token)) {
      nodes.push(
        <span key={i} style={{ whiteSpace: 'nowrap' }}>
          {token}
        </span>,
      );
    } else {
      nodes.push(<React.Fragment key={i}>{token}</React.Fragment>);
    }
  }

  return nodes;
}
